// JavaScript code for the example
let age = 25; // Variable to store the age
let name = 'John'; // Variable to store the name
let isStudent = true; // Variable to store boolean value (true or false)

// Output the variables to the browser's console
console.log('Name:', name);
console.log('Age:', age);
console.log('Is Student:', isStudent);

// Data types
let num = 10;
let str = 'Hello, World!';
let arr = [1, 2, 3, 4, 5];
let obj = { name: 'John', age: 25, isStudent: true };
let isNull = null;
let isUndefined;

console.log('Number:', typeof num);
console.log('String:', typeof str);
console.log('Array:', typeof arr);
console.log('Object:', typeof obj);
console.log('Null:', typeof isNull);
console.log('Undefined:', typeof isUndefined);

